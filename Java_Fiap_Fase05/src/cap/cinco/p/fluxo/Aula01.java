public class Aula01 {

	public static void main(String[] args) {
		
		int idade = 15;

		if (idade >= 18) {
			System.out.println("Maior idade");
		} else if (idade == 18) {
			System.out.println("Acabou de ser maior de idade.");
		}else {	
			System.out.println("Menor idade");
		}

	}

}
